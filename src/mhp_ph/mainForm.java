    public void quickSearch(String text)
    {
        if (tabs == 5)
        {
            PHtooManyTabs p = new PHtooManyTabs();
            p.setVisible(true);
            return;
        }
        turnQuickOn();
        PHresults tab = null;
        PHtab button = null;
        switch (++tabs)
        {
            case 1:
            {
                tab = tab1;
                button = tabButton1;
                currentTab = 1;
                currentTabRef = tab1;
                break;
            }
            case 2:
            {
                tab = tab2;
                button = tabButton2;
                currentTab = 2;
                currentTabRef = tab2;
                break;
            }
            case 3:
            {
                tab = tab3;
                button = tabButton3;
                currentTab = 3;
                currentTabRef = tab3;
                break;
            }
            case 4:
            {
                tab = tab4;
                button = tabButton4;
                currentTab = 4;
                currentTabRef = tab4;
                break;
            }
            case 5:
            {
                tab = tab5;
                button = tabButton5;
                currentTab = 5;
                currentTabRef = tab5;
                break;
            }
        }
        button.setText(text);
        button.setVisible(true);
        deactivateTabs();
        hideTabs();
        tab.setVisible(true);
        button.activate(true);
        try
        {
            tab.getPanel().removeAll();
            LinkedList<phArt> ll = new LinkedList<phArt>();
            setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
            ll = db.generalSearch(text);
            for (phArt e : ll )
            {
                tab.addArt(e);
            }
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            tab.repaint();
        }
        catch (Exception e)
        {
            System.out.println(e.getMessage());
        }
    }
